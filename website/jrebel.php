<p>
JRebel from ZeroTurnaround is a software product which can speed up the reloading of Java and Scala classes. This makes it possible so that
  SLiM Stack does not need to take so long to reload your changed classes, this can usually save a few seconds each time a Scala class is changed.
</p>
<p>
JRebel can be downloaded from the <a href="http://www.zeroturnaround.com/jrebel/current/">JRebel website here</a>, use the 30 day free trail or to extend your trial then apply for the free Scala licence.
 Place the jrebel.jar file in the jrebel directory within the SLiM Stack directory. For example, if you installed SLiM Stack here: C:\Program Files\SLiM Stack then place jrebel.jar within
    C:\Program Files\SLiM Stack\jrebel, the entire jrebel directory can be extracted into this directory as long as the jrebel jar is in the exact location of C:\Program Files\SLiM Stack\jrebel if
    you installed SLiM Stack in the program files.
</p>
<p>
  You must re-start SLiM Stack for the jrebel.jar file to be used by SLiM Stack.
</p>
<p>
  You can obtain a free licence for use of JRebel from the JRebel website by clicking on <b>Get JRebel for free</b> on the right hand side of <a href="http://sales.zeroturnaround.com/">this page</a>. The
    licence file should be placed in the same directory as the jrebel.jar file.
</p>
